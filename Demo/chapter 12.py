# 创建虚拟环境（http://www.pythondoc.com/pythontutorial3/venv.html）
# 创建虚拟环境命令：python3.6 -m venv (directory name) (pyvenv directory name)
# 创建完虚拟环境需要激活：
# directory name/Scripts/activate（Window）
# source directory name/bin/activate
# pip 命令
# 1、pip search astronomy：搜索
# 2、pip install (package name)[==version code]：安装指定包名
# 3、pip install --upgrade package name：更新指定包
# 4、pip uninstall (package name)：移除包
# 5、pip show (package name)：显示指定包的信息
# 6、pip list：列出安装在虚拟环境的包
# 7、pip freeze：生成需要安装的包的列表
